def add(num1, num2):
    return num1 + num2

def sub(num1, num2):
    return num1 - num2

def mul(num1, num2):
    return num1*num2

def div(num1, num2):
    return num1/num2

# בדיקה של הפונקציות
print("--- My Calculator Results ---")
print("Addition (10 + 5):", add(10, 5))
print("Subtraction (10 - 5):", sub(10, 5))
print("Multiplication (10 * 5):", mul(10, 5))
print("Division (10 / 5):", div(10, 5))
